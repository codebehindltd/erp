    public partial class CallToActionInformation : System.Web.UI.Page
    {
        HiddenField innboardMessage;
        HMUtility hmUtility = new HMUtility();
        protected void Page_Load(object sender, EventArgs e)
        {
            innboardMessage = (HiddenField)Master.FindControl("InnboardMessageHiddenField");
        }
        [WebMethod]
        public static GridViewDataNPaging<CallToActionDetailBO, GridPaging> SearchCallToAction(string callToActionFor, string fromDate, string toDate, string company, string contact, int gridRecordsCount, int pageNumber, int isCurrentOrPreviousPage)
        {

            UserInformationBO userInformationBO = new UserInformationBO();
            userInformationBO = System.Web.HttpContext.Current.Session["UserInformationBOSession"] as UserInformationBO;

            int totalRecords = 0;

            GridViewDataNPaging<CallToActionDetailBO, GridPaging> myGridData = new GridViewDataNPaging<CallToActionDetailBO, GridPaging>(userInformationBO.GridViewPageSize, userInformationBO.GridViewPageLink, isCurrentOrPreviousPage);
            pageNumber = myGridData.PageNumberCalculation(gridRecordsCount, pageNumber);
            List<CallToActionDetailBO> contactInformation = new List<CallToActionDetailBO>();
            CallToActionDA DA = new CallToActionDA();
            contactInformation = DA.GetCallToActionPagination("", callToActionFor, fromDate, toDate, company, contact, userInformationBO.GridViewPageSize, pageNumber, out totalRecords);

            myGridData.GridPagingProcessing(contactInformation, totalRecords);

            return myGridData;
        }
    }
}